# if dan else statement
# if dan else statement digunakan untuk menguji kondisi tertentu dan melakukan sesuatu berdasarkan hasilnya.
# contoh 1
x = 10
y = 20
print("1".center(10,'='))
if x > y:
    print("x lebih besar dari y")
else:
    print("x tidak lebih besar dari y")
    
# contoh 2
x = 10
y = 20
print("\n"+"2".center(10,'='))
if x > y:
    print("x lebih besar dari y")
elif x == y:
    print("x sama dengan y")
else:
    print("x tidak lebih besar dari y")
    
